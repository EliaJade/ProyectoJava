package es.cursojava.inicio.objetos.ejercicios.tiendaRopa;

public class Price {
	
	private int ten = 10;
	private int twenty = 20;
	private int thirty = 30;
	
	
	//GET & SET
	public int getTen() {
		return ten;
	}
	public void setTen(int ten) {
		this.ten = ten;
	}
	public int getTwenty() {
		return twenty;
	}
	public void setTwenty(int twenty) {
		this.twenty = twenty;
	}
	public int getThirty() {
		return thirty;
	}
	public void setThirty(int thirty) {
		this.thirty = thirty;
	}
	
	
}
